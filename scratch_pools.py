# We define a helper to clean/normalize keys
def get_clean_key(label):
    raw = str(label or "").strip().lower()
    cleaned = "".join(ch if ch.isalnum() else "_" for ch in raw)
    while "__" in cleaned:
        cleaned = cleaned.replace("__", "_")
    return cleaned.strip("_")

